public class AreaOfRectangle{
    public static void main(String[] args){
        int length = 10;
        int breadth = 5;
        int AreaOfRectangle;

        AreaOfRectangle = length * breadth;
        System.out.println("The Area Of Rectangle of " + length + " and " + breadth + " is " + AreaOfRectangle);
    }
}