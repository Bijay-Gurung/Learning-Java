public class arrays{
    public static void main(String[] args){
        String[] sports = {"Football", "Volleyball", "Basketball", "Tennis"};
        for(String i : sports){
            System.out.println(i);
        }
        System.out.println(sports[0]);

        int[] numbers = {1,2,3,4,5};
        for(int j : numbers){
            System.out.println(j);
        }
    }
}